package Modelo;

/**
 *
 * @author AJ-Barreto
 */
public class Producto {
    int id;
    String nom;
    double precio;
    int stock;
    String estado;

    public Producto() {
    }

    public Producto(int id, String nom, double precio, int stock, String estado) {
        this.id = id;
        this.nom = nom;
        this.precio = precio;
        this.estado = estado;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
    
    
}
